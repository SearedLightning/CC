-- ** Text Wireless Broadcasting System (Server) ** --

-- User inputs text and the computer stores the text in memory.
-- User inputs text color and text size.
-- Computer looks for wireless modems on the computer and wraps them.
-- Computer opens modem to send channel, with a different reply channel for feedback from client computers.
-- Computer transmits user-inputted text to all modems within that send channel. Client computer should take these variables and "wrap" them within the monitor output code.
